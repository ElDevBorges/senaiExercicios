package model;

public class Carro {
	private String fabricante;
	private String modelo;
	private String cor;
	private String ano;
	
	public Carro (String fabricante, String modelo, String cor, String ano) {
		this.fabricante = fabricante;
		this.modelo = modelo;
		this.cor = cor;
		this.ano = ano;
}
}
	



