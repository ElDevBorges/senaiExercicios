package model;

public class Carro {
	public String fabricante;
	public String modelo;
	public String cor;
	public String ano;
	
	public void montarCarro (String fabricante, String modelo, String cor, String ano) {
		this.fabricante = fabricante;
		this.modelo = modelo;
		this.cor = cor;
		this.ano = ano;
}
}
	



